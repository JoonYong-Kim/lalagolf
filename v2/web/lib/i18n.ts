export type MessageKey = keyof typeof messages.en;

export function useI18n() {
  const [locale, setLocaleState] = useState<Locale>("ko");

  useEffect(() => {
    const saved = window.localStorage.getItem(localeKey);
    if (saved === "ko" || saved === "en") setLocaleState(saved);
  }, []);

  function setLocale(nextLocale: Locale) {
    setLocaleState(nextLocale);
    window.localStorage.setItem(localeKey, nextLocale);
    window.dispatchEvent(new CustomEvent("golfraiders-locale-change", { detail: nextLocale }));
  }

  useEffect(() => {
    function handleLocaleChange(event: Event) {
      const nextLocale = (event as CustomEvent<Locale>).detail;
      if (nextLocale === "ko" || nextLocale === "en") setLocaleState(nextLocale);
    }
    window.addEventListener("golfraiders-locale-change", handleLocaleChange);
    return () => window.removeEventListener("golfraiders-locale-change", handleLocaleChange);
  }, []);

  const t = (key: MessageKey) => messages[locale][key];
  return { locale, setLocale, t };
}
